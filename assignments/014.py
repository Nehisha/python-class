# Greeting Message
# Use string formatting to create a function greet_user that takes
# a name and an age and returns a string like
# "Hello, [name]! You are [age] years old."

def greet_user(name, age):
    return f"Hello,{name}! You are {age} years old."


print(greet_user("Nehisha", 19))
print(greet_user("Simran", 20))
