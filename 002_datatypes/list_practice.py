# 1.Write a Python program to sum all the items in a list.

numbers = [1, 2, 3, 4, 5]

total = sum(numbers)
print(f"Sum of all items in the list is: {total}")

# 2.Write a Python program to multiply all the items in a list.

num = [1, 2, 3, 4, 5]
results = 1
for numbers in num:
    results *= numbers

print(results)

